    private void updateContactsTable(Donor donor) {
        // Modelo de tabela
        DefaultTableModel model = (DefaultTableModel) tableContacts.getModel();
        // Limpa a tabela
        model.setRowCount(0);
        // Adiciona os números de telefone do doador
        for (DonorContact contact : donor.getContacts()) { // Supondo que o método getPhones() retorna uma lista de Strings
            model.addRow(new Object[]{contact.getPhone()});
        }
    }

